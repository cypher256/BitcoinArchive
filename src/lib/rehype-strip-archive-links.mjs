/**
 * rehype-strip-archive-links.mjs — Non-link external URLs in verbatim entries
 *
 * Verbatim primary records (forum posts, mailing-list emails, private
 * correspondence) contain external URLs as part of the historical text.
 * Those URLs were not placed there to serve as 2026-reader navigation —
 * they are the authored text of a 2008-2014 document. Treating them as
 * clickable creates the false expectation that they still work, which is
 * untrue at scale (cypherpunk-era and bitcoin.org-era URLs have already
 * suffered link rot in many entries).
 *
 * This plugin strips clickability from http(s) external links inside
 * verbatim entries. The URL text remains visible (and copy-pasteable),
 * but the user is not invited to click. This matches the practice of
 * print archives, government repositories, and academic citation
 * conventions: historical URLs are part of the text, not navigation.
 *
 * Scope:
 *   - Applied only to files under src/data/{entries/en, translations/ja}/
 *     {forum, correspondence, emails}/...
 *   - Only http:// and https:// hrefs are stripped. mailto: / bitcoin: /
 *     ftp: and other URI schemes are left as-is (they are explanatory
 *     content in some entries, e.g. the bitcoin: URI scheme proposal).
 *   - Internal archive links (/BitcoinArchive/... or /... after
 *     remark-rewrite-base) and anchor links (#section) do not match the
 *     http(s) scheme filter, so they are excluded automatically.
 *   - Editor-note blocks (<aside class="editor-inline ...">, generated by
 *     remark-editorial-marker.mjs from *[Context: ...]* / *[編者注：...]*
 *     paragraphs) are exempted: those are editorial overlay, not part of
 *     the verbatim text.
 *
 * Output:
 *   - When the link text equals the href (bare URL link, e.g.
 *     [http://x](http://x)), the URL is rendered once as
 *     <code class="archive-url-bare">http://x</code>.
 *   - Otherwise the link is rendered as
 *     <span class="archive-url">{children} (<code class="archive-url-bare">href</code>)</span>
 *     where {children} preserves the original link's HAST children
 *     (so inline emphasis, code, etc. inside the link text are kept).
 *
 * Pipeline order:
 *   This plugin must run AFTER remark-editorial-marker.mjs (which
 *   converts *[Context: ...]* paragraphs into <aside.editor-inline>),
 *   because editor-note exemption is detected via that <aside> structure.
 *   The Astro config places remark plugins before rehype plugins, so
 *   ordering is satisfied as long as both stay in their respective phases.
 */
import { visitParents } from 'unist-util-visit-parents';
import { toText } from 'hast-util-to-text';

// Path fragments that mark a file as a verbatim primary record. Forward
// slashes are used so the match is portable across platforms — vfile.path
// from Astro is normalized to forward slashes.
const VERBATIM_DIRS = ['/forum/', '/correspondence/', '/emails/'];

// Only http(s) external links are subject to non-linking. Internal links
// (which start with `/`), anchor links (`#...`), and other URI schemes
// (mailto:, bitcoin:, ftp:, etc.) all fail this test and are skipped.
const HTTP_SCHEME = /^https?:\/\//i;

/**
 * Detect whether a node is inside an editor-note aside.
 *
 * remark-editorial-marker.mjs converts paragraphs that consist of a
 * single italic-bracketed editorial marker (*[Context: ...]* /
 * *[編者注：...]* / *[補足：...]* / *[Editor: ...]*) into
 * <aside class="editor-inline editor-inline--{kind}">, stripping the
 * bracket prefix and trailing `]`. This means by the time rehype runs:
 *   - The original <em> element no longer exists; <aside> wraps the
 *     post-strip content.
 *   - The text no longer starts with "[Context:" etc.
 * So editor-note detection at the rehype stage must use the <aside>
 * structural marker, not text-prefix matching.
 */
function isInsideEditorNote(ancestors) {
  for (let i = ancestors.length - 1; i >= 0; i--) {
    const node = ancestors[i];
    if (node.type !== 'element' || node.tagName !== 'aside') continue;
    const classNames = node.properties?.className;
    if (Array.isArray(classNames) && classNames.includes('editor-inline')) {
      return true;
    }
  }
  return false;
}

export function rehypeStripArchiveLinks() {
  return (tree, vfile) => {
    const filePath = vfile.path || '';
    if (!VERBATIM_DIRS.some((d) => filePath.includes(d))) return;

    visitParents(tree, 'element', (node, ancestors) => {
      if (node.tagName !== 'a') return;
      const href = node.properties?.href;
      if (!href || typeof href !== 'string') return;
      // http(s) 以外 (内部リンク・アンカー・mailto: / bitcoin: 等) は対象外
      if (!HTTP_SCHEME.test(href)) return;
      // 編者注内の外部リンクはクリッカブル維持
      if (isInsideEditorNote(ancestors)) return;

      const parent = ancestors[ancestors.length - 1];
      if (!parent || !Array.isArray(parent.children)) return;
      const index = parent.children.indexOf(node);
      if (index === -1) return;

      // bare URL link (link text == href) は URL を 1 回だけ表示する
      const linkText = toText(node, { whitespace: 'normal' }).trim();
      const isBareUrl = linkText === href.trim();

      if (isBareUrl) {
        parent.children[index] = {
          type: 'element',
          tagName: 'code',
          properties: { className: ['archive-url-bare'] },
          children: [{ type: 'text', value: href }],
        };
      } else {
        parent.children[index] = {
          type: 'element',
          tagName: 'span',
          properties: { className: ['archive-url'] },
          children: [
            // 元のリンク子要素 (装飾を含む) をそのまま流用
            ...node.children,
            { type: 'text', value: ' (' },
            {
              type: 'element',
              tagName: 'code',
              properties: { className: ['archive-url-bare'] },
              children: [{ type: 'text', value: href }],
            },
            { type: 'text', value: ')' },
          ],
        };
      }
    });
  };
}

export default rehypeStripArchiveLinks;
