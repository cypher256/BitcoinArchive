#!/usr/bin/env node
/**
 * generate-derived-commentaries.mjs — Reverse index of "this primary-source
 * entry has commentaries that discuss it" relationships.
 *
 * Produces src/data/derived-commentaries.json. Keyed by primary-source
 * entry id (correspondence / mailing-list / forum-post / bip / whitepaper
 * / court-document); each value is an array of editorial commentary
 * entries (type=article or type=analysis) whose frontmatter
 * quotes[].sourceEntryId points at that primary source.
 *
 * Initial-implementation scope (per temp/0521_発言_解説_双方向リンク計画.md):
 *   - Source of the relation: solely quotes[].sourceEntryId.
 *     relatedEntries-based commentary inference is deferred.
 *   - Commentary types: type=article and type=analysis only.
 *     type=biography is excluded — bio entries discuss persons, not
 *     individual messages, and including them produces noise.
 *   - via field is a forward-looking string ("quote-source" today,
 *     room for "related-entries" in a later phase).
 *
 * Integrity (fail fast, no silent fallback):
 *   - Commentary's sourceEntryId target must exist on the EN side.
 *   - Both the commentary entry and the primary-source target must
 *     also exist on the JA side (src/data/translations/ja/). Missing
 *     mirror is treated as a hard error — process.exit(1) — to surface
 *     mirror drift at build time rather than rendering title.ja =
 *     title.en silently. Archive is operated as a fully-mirrored EN/JA
 *     corpus; if a mirror is missing, the editor needs to add it
 *     rather than have the UI mask the absence.
 *
 * Output is git-ignored; regenerated by `npm run check` / `npm run dev`
 * / `npm run build` hooks (same pattern as derived-related.json and
 * git-dates.json).
 */

import { readdirSync, readFileSync, statSync, existsSync, writeFileSync, mkdirSync } from 'fs';
import path from 'path';

const EN_DIR = 'src/data/entries/en';
const JA_DIR = 'src/data/translations/ja';
const OUTPUT = 'src/data/derived-commentaries.json';

const COMMENTARY_TYPES = new Set(['article', 'analysis']);
const PRIMARY_SOURCE_TYPES = new Set([
  'correspondence',
  'mailing-list',
  'forum-post',
  'bip',
  'whitepaper',
  'court-document',
]);

// ---------------------------------------------------------------------------
// File / frontmatter helpers (mirrors generate-derived-related.mjs)
// ---------------------------------------------------------------------------

function walk(dir) {
  const results = [];
  if (!existsSync(dir)) return results;
  for (const entry of readdirSync(dir).sort()) {
    const full = path.join(dir, entry);
    if (statSync(full).isDirectory()) results.push(...walk(full));
    else if (full.endsWith('.md')) results.push(full);
  }
  return results;
}

/**
 * Extract leading YAML frontmatter using the same regex pattern as
 * generate-derived-related.mjs. Body-level `---` (horizontal rules) do
 * not interfere because we only consume the first occurrence.
 */
function extractFrontmatter(content) {
  const m = content.match(/^---\n([\s\S]*?)\n---/);
  return m ? m[1] : null;
}

/**
 * Read a single top-level scalar field. Pattern matches the simple
 * `key: value` and `key: "value"` shapes used in our frontmatter.
 */
function readScalar(fm, key) {
  const re = new RegExp(`^${key}:\\s*"?([^"\\n]*?)"?\\s*$`, 'm');
  const m = fm.match(re);
  return m ? m[1].trim() : null;
}

/**
 * Read the `title:` field, peeling outer quotes if present. Allowed to
 * contain spaces and commas; falls back to null if missing.
 */
function readTitle(fm) {
  // title: "..." or title: ...
  const m = fm.match(/^title:\s*"((?:[^"\\]|\\.)*)"\s*$/m) || fm.match(/^title:\s*(.+?)\s*$/m);
  if (!m) return null;
  // Unescape backslash-quote inside the captured string for the quoted form.
  return m[1].replace(/\\"/g, '"');
}

/**
 * Parse the YAML `relatedEntries:` block. Returns an array of entry-id
 * strings. Handles both `  - "id"` and `  - id` shapes. Stops when it
 * hits the next top-level key or end of frontmatter.
 */
function parseRelatedEntriesBlock(fm) {
  const lines = fm.split('\n');
  const startIdx = lines.findIndex((l) => /^relatedEntries:\s*$/.test(l));
  if (startIdx === -1) return [];

  const ids = [];
  for (let i = startIdx + 1; i < lines.length; i++) {
    const line = lines[i];
    if (/^\S/.test(line)) break; // next top-level key
    if (/^\s*$/.test(line)) continue;
    const m = line.match(/^\s+-\s+"?([^"\n]+?)"?\s*$/);
    if (m) ids.push(m[1].trim());
  }
  return ids;
}

/**
 * Parse the YAML `quotes:` block. Returns an array of
 * { id, personSlug, sourceEntryId } objects. We only need fields used by
 * downstream consumers; other quote fields (date, parent, etc.) are
 * ignored here.
 */
function parseQuotesBlock(fm) {
  const lines = fm.split('\n');
  const startIdx = lines.findIndex((l) => /^quotes:\s*$/.test(l));
  if (startIdx === -1) return [];

  const quotes = [];
  let cur = null;
  for (let i = startIdx + 1; i < lines.length; i++) {
    const line = lines[i];
    // End of block: another top-level key (no leading whitespace + `:`)
    if (/^\S/.test(line)) break;
    if (/^\s*$/.test(line)) continue;

    const newItem = line.match(/^\s+-\s+(?:id:\s*)?"?([^"\n]*?)"?\s*$/);
    if (newItem && /^\s+-\s+id:/.test(line)) {
      if (cur) quotes.push(cur);
      cur = { id: newItem[1], personSlug: null, sourceEntryId: null };
      continue;
    }
    if (!cur) continue;

    const slug = line.match(/^\s+personSlug:\s*"?([^"\n]+?)"?\s*$/);
    if (slug) {
      cur.personSlug = slug[1];
      continue;
    }
    const sei = line.match(/^\s+sourceEntryId:\s*"?([^"\n]*?)"?\s*$/);
    if (sei) {
      const value = sei[1].trim();
      // Treat YAML null literal and empty quoted string as "no source
      // entry id". Both shapes occur in practice (`sourceEntryId: null`
      // and `sourceEntryId: ""`) and must not be lifted into the map
      // as if they were entry ids.
      cur.sourceEntryId = value === 'null' || value === '' ? null : value;
      continue;
    }
  }
  if (cur) quotes.push(cur);
  return quotes;
}

function computeEntryId(relPath) {
  const withoutExt = relPath.replace(/\.md$/, '');
  const parts = withoutExt.split(path.sep);
  const basename = parts.pop();
  return [...parts, basename.replaceAll('.', '')].join('/');
}

/**
 * Locate the source markdown file for an entry id. The entry id has dots
 * stripped from its basename (see computeEntryId); the on-disk file
 * basename may or may not contain dots. Resolve by checking both shapes.
 */
function findSourceFile(rootDir, entryId) {
  const parts = entryId.split('/');
  const basename = parts.pop();
  const dir = path.join(rootDir, ...parts);
  if (!existsSync(dir)) return null;
  for (const candidate of readdirSync(dir)) {
    if (!candidate.endsWith('.md')) continue;
    const normalized = candidate.replace(/\.md$/, '').replaceAll('.', '');
    if (normalized === basename) return path.join(dir, candidate);
  }
  return null;
}

// ---------------------------------------------------------------------------
// First pass: index every entry's id, type, and EN title.
// ---------------------------------------------------------------------------

const entryIndex = new Map(); // id -> { type, titleEn, fileRel }

for (const file of walk(EN_DIR)) {
  const rel = path.relative(EN_DIR, file);
  const id = computeEntryId(rel);
  const content = readFileSync(file, 'utf-8');
  const fm = extractFrontmatter(content);
  if (!fm) continue;
  const type = readScalar(fm, 'type');
  const title = readTitle(fm);
  entryIndex.set(id, { type, titleEn: title, fileRel: rel });
}

// ---------------------------------------------------------------------------
// Second pass: collect commentary -> primary-source citations and validate
// EN/JA mirror integrity.
// ---------------------------------------------------------------------------

const issues = [];
// reverse[primaryId] is a Map<commentaryId, { id, type, title, via }> so we
// can short-circuit the duplicate-(primary, commentary) case in O(1) and
// keep the strongest `via` per pair (quote-source > related-entries) by
// adding quote-source candidates first.
const reverse = new Map();

/**
 * Validate the JA mirror of an entry and return its JA title. Logs an
 * integrity issue and returns null on missing mirror or unparseable title.
 */
function requireJaTitle(entryId, context) {
  const jaFile = findSourceFile(JA_DIR, entryId);
  if (!jaFile) {
    issues.push(
      `${context} ${entryId} has no JA mirror under ${JA_DIR}/. ` +
        `EN/JA mirrors must be paired; add the JA translation or remove the EN entry.`,
    );
    return null;
  }
  const jaFm = extractFrontmatter(readFileSync(jaFile, 'utf-8'));
  const jaTitle = jaFm ? readTitle(jaFm) : null;
  if (!jaTitle) {
    issues.push(`${context} ${entryId} JA mirror is missing a parseable title.`);
    return null;
  }
  return jaTitle;
}

/**
 * Add a (primary, commentary, via) link. Quote-source wins over
 * related-entries when the same pair surfaces through both paths; the
 * caller is responsible for processing the quote-source pass first.
 */
function addLink(primaryId, commentaryMeta, via) {
  if (!reverse.has(primaryId)) reverse.set(primaryId, new Map());
  const map = reverse.get(primaryId);
  if (map.has(commentaryMeta.id)) return; // quote-source already claimed this slot
  map.set(commentaryMeta.id, { ...commentaryMeta, via });
}

/**
 * Two passes per commentary entry: first the strong `quotes[].sourceEntryId`
 * declarations, then the broader `relatedEntries` references. The strong
 * pass establishes `via: "quote-source"` first; the broader pass only adds
 * pairs that the strong pass didn't already cover, marking them
 * `via: "related-entries"`.
 */
for (const pass of ['quote-source', 'related-entries']) {
  for (const [commentaryId, meta] of entryIndex) {
    if (!COMMENTARY_TYPES.has(meta.type)) continue;

    const file = findSourceFile(EN_DIR, commentaryId);
    if (!file) continue;
    const content = readFileSync(file, 'utf-8');
    const fm = extractFrontmatter(content);
    if (!fm) continue;

    const targets = new Set();
    if (pass === 'quote-source') {
      for (const q of parseQuotesBlock(fm)) {
        if (q.sourceEntryId) targets.add(q.sourceEntryId);
      }
    } else {
      for (const id of parseRelatedEntriesBlock(fm)) {
        targets.add(id);
      }
    }
    if (targets.size === 0) continue;

    // Resolve the commentary's JA mirror once per pass; the validator
    // memoises nothing but the per-commentary cost is one file read per
    // pass, dwarfed by the rest of the walk.
    const commentaryJaTitle = requireJaTitle(commentaryId, 'commentary');
    if (!commentaryJaTitle) continue;

    const commentaryMeta = {
      id: commentaryId,
      type: meta.type,
      title: {
        en: meta.titleEn || '',
        ja: commentaryJaTitle,
      },
    };

    for (const primaryId of targets) {
      const primaryMeta = entryIndex.get(primaryId);
      if (!primaryMeta) {
        if (pass === 'quote-source') {
          // quotes[].sourceEntryId is a strong declaration — an unresolved
          // target is an editor error. relatedEntries is broader (entries
          // routinely list participant bios, threads, etc.) so an unresolved
          // target there is not a fatal issue for THIS generator: the
          // separate check-internal-links.mjs is the authoritative validator
          // for the relatedEntries graph.
          issues.push(
            `commentary ${commentaryId} cites sourceEntryId ${primaryId} but ` +
              `no such EN entry exists.`,
          );
        }
        continue;
      }
      if (!PRIMARY_SOURCE_TYPES.has(primaryMeta.type)) {
        // Cross references between non-primary types are common (article
        // -> analysis, analysis -> biography, etc.) and not part of the
        // primary-source <-> commentary axis this index models.
        continue;
      }
      // Skip pairs the quote-source pass already locked in.
      if (pass === 'related-entries' && reverse.get(primaryId)?.has(commentaryId)) {
        continue;
      }
      // Validate the primary source's JA mirror (only when it's new to us;
      // if the strong pass already added the pair, we've validated already).
      const primaryJaTitle = requireJaTitle(primaryId, 'primary-source');
      if (!primaryJaTitle) continue;

      addLink(primaryId, commentaryMeta, pass);
    }
  }
}

if (issues.length > 0) {
  console.error(`✗ generate-derived-commentaries: ${issues.length} integrity issue(s):`);
  for (const msg of issues) console.error(`  - ${msg}`);
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Emit deterministic output.
// ---------------------------------------------------------------------------

// Per-primary list ordered by entry date ascending (oldest commentary
// first). We need the commentary's date for that; pick it up from the
// commentary's frontmatter at output time. Tiebreak by id for full
// determinism.
function commentaryDate(commentaryId) {
  const file = findSourceFile(EN_DIR, commentaryId);
  if (!file) return '';
  const fm = extractFrontmatter(readFileSync(file, 'utf-8'));
  if (!fm) return '';
  return readScalar(fm, 'date') || '';
}

const output = {};
// Order per primary:
//   1. via === "quote-source" before via === "related-entries"
//      (the strong direct-citation declaration outranks the broader
//      related-entries graph signal)
//   2. inside the same via, commentary entry date ascending
//      (oldest commentary first — keeps the primary entry that
//      others reference earliest at the top)
//   3. tiebreak by commentary id ascending (full determinism)
const VIA_ORDER = { 'quote-source': 0, 'related-entries': 1 };
const primaryIds = [...reverse.keys()].sort();
for (const primaryId of primaryIds) {
  const list = [...reverse.get(primaryId).values()];
  list.sort((a, b) => {
    const vDiff = VIA_ORDER[a.via] - VIA_ORDER[b.via];
    if (vDiff !== 0) return vDiff;
    const da = commentaryDate(a.id);
    const db = commentaryDate(b.id);
    return da.localeCompare(db) || a.id.localeCompare(b.id);
  });
  output[primaryId] = list;
}

mkdirSync(path.dirname(OUTPUT), { recursive: true });
writeFileSync(OUTPUT, JSON.stringify(output, null, 2) + '\n', 'utf-8');

const primaries = Object.keys(output).length;
const totalLinks = Object.values(output).reduce((s, a) => s + a.length, 0);
console.log(`✓ Derived commentaries written to ${OUTPUT}`);
console.log(`  ${primaries} primary-source entries have at least one commentary`);
console.log(`  ${totalLinks} total commentary links`);
